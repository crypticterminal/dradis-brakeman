class DradisTasks < Thor
  class Upload < Thor
    namespace "dradis:upload"

   desc  "brakeman FILE", "upload Brakeman results in JSON format"
   long_desc "This plugin expect a JSON file generated by Brakeman using: -f jason -o results.json"
   def brakeman(file_path)
     require 'config/environment'

     logger = Logger.new(STDOUT)
     logger.level = Logger::DEBUG

     unless File.exists?(file_path)
       $stderr.puts "** the file [#{file_path}] does not exist"
       exit -1
     end

     BrakemanUpload.import(
       :file => file_path,
       :logger => logger)

     logger.close
   end

  end
end
